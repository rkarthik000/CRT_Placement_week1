#include<stdio.h>

int main(){

    // Integer data type
    int a = 10;
    printf("Integer data type value: %d\n",a);

    // float data type
    float b = 12.123;
    printf("Float data type value: %f\n",b);

    // double data type
    double c = 12.444444;
    printf("Double data type value: %lf\n",c);

    // character data type
    char d = 'a';
    printf("char type: %c\n",d);

}